int	ft_str_is_numeric(char *str)
{
	int	index;
	int	ch;

	index = 0;
	ch = 'e';
	while (str[index] != '\0')
	{
		ch = str[index];
		if (ch < '0' || ch > '9')
			return (0);
		index++;
	}
	return (1);
}
