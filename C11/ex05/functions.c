#include<stdio.h>

int	ft_mult(int a, int b)
{
	return (a * b);
}

int	ft_plus(int a, int b)
{
	return (a + b);
}

int	ft_min(int a, int b)
{
	return (a - b);
}

int	ft_div(int a, int b)
{
	return (a / b);
}

int	ft_mod(int a, int b)
{
	return (a % b);
}
